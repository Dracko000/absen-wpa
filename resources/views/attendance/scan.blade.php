    <script>
        document.addEventListener('DOMContentLoaded', function() {
            const classSelect = document.getElementById('classSelect');
            const dateInput = document.getElementById('dateInput');
            const startScannerBtn = document.getElementById('startScannerBtn');
            const uploadQRBtn = document.getElementById('uploadQRBtn');
            const manualInputBtn = document.getElementById('manualInputBtn');
            const scannerContainer = document.getElementById('scannerContainer');
            const uploadContainer = document.getElementById('uploadContainer');
            const manualInputContainer = document.getElementById('manualInputContainer');
            const captureBtn = document.getElementById('captureBtn');
            const video = document.getElementById('video');
            const canvas = document.getElementById('canvas');
            const qrImageUpload = document.getElementById('qrImageUpload');
            const processUploadBtn = document.getElementById('processUploadBtn');
            const qrInput = document.getElementById('qrInput');
            const submitQRBtn = document.getElementById('submitQRBtn');
            const resultContainer = document.getElementById('resultContainer');
            const resultDiv = document.getElementById('result');
            const attendanceTable = document.getElementById('attendanceTable');
            const attendanceTableBody = document.getElementById('attendanceTableBody');
            const scannerStatus = document.getElementById('scannerStatus');

            // When date changes, fetch attendance (no class selection needed anymore)
            dateInput.addEventListener('change', fetchAttendance);

            // Show manual input form
            manualInputBtn.addEventListener('click', function() {
                if (!dateInput.value) {
                    alert('Please select a date first');
                    return;
                }

                manualInputContainer.style.display = 'block';
                scannerContainer.style.display = 'none';
                uploadContainer.style.display = 'none';
            });

            // Show upload form
            uploadQRBtn.addEventListener('click', function() {
                if (!dateInput.value) {
                    alert('Please select a date first');
                    return;
                }

                scannerContainer.style.display = 'none';
                manualInputContainer.style.display = 'none';
                uploadContainer.style.display = 'block';
            });

            // Process uploaded QR image
            processUploadBtn.addEventListener('click', function() {
                const file = qrImageUpload.files[0];
                if (!file) {
                    resultDiv.innerHTML = '<p class="text-red-600">Please select a QR code image file</p>';
                    resultContainer.style.display = 'block';
                    return;
                }

                // Check if the file is an image
                if (!file.type.match('image.*')) {
                    resultDiv.innerHTML = '<p class="text-red-600">Please select an image file (JPEG, PNG, etc.)</p>';
                    resultContainer.style.display = 'block';
                    return;
                }

                // Show loading indicator
                resultDiv.innerHTML = '<p class="text-blue-600">Processing image, please wait...</p>';
                resultContainer.style.display = 'block';

                const reader = new FileReader();
                reader.onload = function(e) {
                    const img = new Image();
                    img.onload = function() {
                        // Create a canvas to draw the image and scan for QR codes
                        const canvas = document.createElement('canvas');
                        const ctx = canvas.getContext('2d');

                        // Calculate optimal dimensions to speed up processing
                        const maxWidth = 800;
                        const maxHeight = 600;
                        let { width, height } = img;

                        // Calculate aspect ratio to maintain image proportions
                        if (width > maxWidth) {
                            height = (height * maxWidth) / width;
                            width = maxWidth;
                        }
                        if (height > maxHeight) {
                            width = (width * maxHeight) / height;
                            height = maxHeight;
                        }

                        canvas.width = width;
                        canvas.height = height;
                        ctx.drawImage(img, 0, 0, width, height);

                        const imageData = ctx.getImageData(0, 0, canvas.width, canvas.height);
                        const code = jsQR(imageData.data, imageData.width, imageData.height);

                        if (code) {
                            // Check if code.data is not empty before processing
                            if (code.data && code.data.trim() !== '') {
                                resultDiv.innerHTML = '<p>Scanned: ' + code.data + '</p>';
                                console.log('Uploaded QR data:', code.data); // Debug log
                                processScan(code.data);
                            } else {
                                resultDiv.innerHTML = '<p class="text-orange-600">QR detected in image but data is empty. Please try another image with a valid student QR code.</p>';
                            }
                        } else {
                            resultDiv.innerHTML = '<p class="text-red-600">No QR code detected in the uploaded image. Please try another image.</p>';
                        }
                    };
                    img.src = e.target.result;
                };
                reader.readAsDataURL(file);
            });

            // Initialize camera scanner
            startScannerBtn.addEventListener('click', function() {
                if (!dateInput.value) {
                    alert('Please select a date first');
                    return;
                }

                manualInputContainer.style.display = 'none';
                uploadContainer.style.display = 'none';
                scannerContainer.style.display = 'block';
                initCameraScanner();
            });

            // Submit manually entered QR code
            submitQRBtn.addEventListener('click', function() {
                const qrData = qrInput.value.trim();
                if (!qrData) {
                    resultDiv.innerHTML = '<p class="text-red-600">Please enter QR code data</p>';
                    resultContainer.style.display = 'block';
                    return;
                }

                processScan(qrData);
            });

            // Process QR code when captured
            captureBtn.addEventListener('click', function() {
                const videoWidth = video.videoWidth;
                const videoHeight = video.videoHeight;

                // Use optimized dimensions for capture
                const maxWidth = 800;
                const maxHeight = 600;
                let width = videoWidth;
                let height = videoHeight;

                // Calculate aspect ratio to maintain image proportions
                if (width > maxWidth) {
                    height = (height * maxWidth) / width;
                    width = maxWidth;
                }
                if (height > maxHeight) {
                    width = (width * maxHeight) / height;
                    height = maxHeight;
                }

                canvas.width = width;
                canvas.height = height;

                const context = canvas.getContext('2d');
                context.drawImage(video, 0, 0, canvas.width, canvas.height);

                const imageData = context.getImageData(0, 0, canvas.width, canvas.height);
                const code = jsQR(imageData.data, imageData.width, imageData.height);

                if (code) {
                    console.log('Raw QR code data:', code.data); // Debug log
                    // Check if code.data is not empty before processing
                    if (code.data && code.data.trim() !== '') {
                        resultDiv.innerHTML = '<p>Scanned: ' + code.data + '</p>';
                        resultContainer.style.display = 'block';
                        processScan(code.data);
                    } else {
                        resultDiv.innerHTML = '<p class="text-orange-600">QR detected but data is empty. Please try again with a valid student QR code.</p>';
                        resultContainer.style.display = 'block';
                    }
                } else {
                    resultDiv.innerHTML = '<p class="text-red-600">No QR code detected. Please try again.</p>';
                    resultContainer.style.display = 'block';
                }
            });

            function initCameraScanner() {
                if (navigator.mediaDevices && navigator.mediaDevices.getUserMedia) {
                    navigator.mediaDevices.getUserMedia({ video: { facingMode: "environment" } })
                        .then(function(stream) {
                            video.srcObject = stream;
                            video.setAttribute("playsinline", true); // required to tell iOS safari we don't want fullscreen
                            video.play();
                            scannerStatus.textContent = 'Camera active - Point at QR code';

                            // Start QR code detection
                            requestAnimationFrame(tick);
                        })
                        .catch(function(err) {
                            console.error("Error accessing camera: ", err);
                            resultDiv.innerHTML = '<p class="text-red-600">Error accessing camera: ' + err.message + '</p>';
                            resultContainer.style.display = 'block';
                        });
                } else {
                    resultDiv.innerHTML = '<p class="text-red-600">Camera not supported in this browser</p>';
                    resultContainer.style.display = 'block';
                }
            }

            function tick() {
                if (video.readyState === video.HAVE_ENOUGH_DATA) {
                    // Use smaller canvas dimensions for faster processing
                    const videoWidth = video.videoWidth;
                    const videoHeight = video.videoHeight;

                    // Only resize canvas if it's significantly different from video dimensions
                    if (canvas.width !== videoWidth || canvas.height !== videoHeight) {
                        // Calculate optimal dimensions to speed up processing
                        const maxWidth = 640;  // Smaller than before for faster processing
                        const maxHeight = 480;
                        let width = videoWidth;
                        let height = videoHeight;

                        // Calculate aspect ratio to maintain image proportions
                        if (width > maxWidth) {
                            height = (height * maxWidth) / width;
                            width = maxWidth;
                        }
                        if (height > maxHeight) {
                            width = (width * maxHeight) / height;
                            height = maxHeight;
                        }

                        canvas.width = width;
                        canvas.height = height;
                    }

                    const context = canvas.getContext('2d');
                    context.drawImage(video, 0, 0, canvas.width, canvas.height);
                    const imageData = context.getImageData(0, 0, canvas.width, canvas.height);
                    const code = jsQR(imageData.data, imageData.width, imageData.height);

                    if (code) {
                        // Highlight the QR code area
                        context.lineWidth = 2;
                        context.strokeStyle = "#00FF00";
                        // Adjust highlight position based on scaled coordinates
                        const scaleX = canvas.width / videoWidth;
                        const scaleY = canvas.height / videoHeight;
                        context.strokeRect(
                            code.location.topLeftCorner.x * scaleX,
                            code.location.topLeftCorner.y * scaleY,
                            (code.location.topRightCorner.x - code.location.topLeftCorner.x) * scaleX,
                            (code.location.bottomRightCorner.y - code.location.topLeftCorner.y) * scaleY
                        );

                        console.log('Raw QR code data:', code.data); // Debug log
                        // Check if code.data is not empty before processing
                        if (code.data && code.data.trim() !== '') {
                            // Display captured code
                            resultDiv.innerHTML = '<p>Scanned: ' + code.data + '</p>';
                            resultContainer.style.display = 'block';
                            captureBtn.style.display = 'block';

                            console.log('Detected QR data:', code.data); // Debug log

                            // Process the QR code immediately
                            processScan(code.data);
                        } else {
                            resultDiv.innerHTML = '<p class="text-orange-600">QR detected but data is empty. Please try again with a valid student QR code.</p>';
                            resultContainer.style.display = 'block';
                            captureBtn.style.display = 'none';
                        }
                    } else {
                        captureBtn.style.display = 'none';
                    }
                }
                requestAnimationFrame(tick);
            }

            function processScan(qrData) {
                // Only proceed if qrData is not empty
                if (!qrData || qrData.trim() === '') {
                    resultDiv.innerHTML = '<p class="text-red-600">Error: No QR code data detected. Please make sure the QR code is properly positioned.</p>';
                    resultContainer.style.display = 'block';
                    return;
                }

                // Try to parse the QR data as JSON to verify it's in the expected format
                try {
                    const parsedData = JSON.parse(qrData);
                    console.log('Parsed QR data:', parsedData); // Debug log
                } catch (e) {
                    resultDiv.innerHTML = '<p class="text-red-600">Error: Invalid QR code format. Please use a valid student QR code from the system.</p>';
                    resultContainer.style.display = 'block';
                    console.error('QR data parsing error:', e);
                    return;
                }

                // Show validation indicator
                resultDiv.innerHTML = '<p class="text-blue-600">Validating QR code data...</p>';

                fetch('/attendance/process-scan', {
                    method: 'POST',
                    headers: {
                        'Content-Type': 'application/json',
                        'X-CSRF-TOKEN': document.querySelector('meta[name="csrf-token"]').getAttribute('content')
                    },
                    body: JSON.stringify({
                        qr_data: qrData,
                        date: dateInput.value
                    })
                })
                .then(response => response.json())
                .then(data => {
                    if (data.success) {
                        resultDiv.innerHTML += '<p class="text-green-600">' + data.message + '</p>';
                        if (data.student) {
                            resultDiv.innerHTML += '<p class="text-blue-600">Student: ' + data.student.name + ' (' + data.student.email + ')</p>';
                        } else if (data.admin) {
                            resultDiv.innerHTML += '<p class="text-blue-600">Admin: ' + data.admin.name + ' (' + data.admin.email + ')</p>';
                        }
                        // Refresh attendance list
                        fetchAttendance();
                    } else if (data.show_status_modal) {
                        // Show the status selection modal to allow changing status
                        resultDiv.innerHTML += '<p class="text-green-600">' + data.message + '</p>';
                        if (data.student) {
                            resultDiv.innerHTML += '<p class="text-blue-600">Student: ' + data.student.name + ' (' + data.student.email + ') - Current Status: ' + data.current_status + '</p>';
                        } else if (data.admin) {
                            resultDiv.innerHTML += '<p class="text-blue-600">Admin: ' + data.admin.name + ' (' + data.admin.email + ') - Current Status: ' + data.current_status + '</p>';
                        }

                        // Determine if this is a student or admin for the modal
                        const isStudent = !!data.student;
                        const userData = isStudent ? data.student : data.admin;

                        document.getElementById('studentInfo').innerHTML =
                            '<p class="font-medium">' + (isStudent ? 'Student' : 'Admin') + ': <strong>' + userData.name + '</strong></p>' +
                            '<p>Email: ' + userData.email + '</p>' +
                            '<p>Class: ' + (userData.class || 'N/A') + '</p>' +
                            '<p>Current Status: <span id="currentStatus" class="font-bold">' + data.current_status.toUpperCase() + '</span></p>';

                        // Store the QR data and date for later use
                        window.currentQRData = qrData;
                        window.currentDate = dateInput.value;
                        window.currentStudentId = userData.id; // This should work for both student and admin

                        // Show the status modal
                        document.getElementById('statusModal').style.display = 'block';

                        // Refresh attendance list
                        fetchAttendance();
                    } else {
                        resultDiv.innerHTML += '<p class="text-red-600">Error: ' + data.message + '</p>';
                        if (data.message.includes('Invalid QR code')) {
                            resultDiv.innerHTML += '<p class="text-sm text-gray-600 mt-2">Make sure you are scanning a valid student QR code generated from the system.</p>';
                        }
                    }
                })
                .catch(error => {
                    resultDiv.innerHTML += '<p class="text-red-600">Error: ' + error.message + '</p>';
                    console.error('Scan error:', error);
                });
            }

            function fetchAttendance() {
                if (!dateInput.value) {
                    return;
                }

                // For now, we'll fetch all attendance for the selected date
                // In a real implementation, you might want to show attendance for all classes or filter differently
                fetch('/attendance/get', {
                    method: 'POST',
                    headers: {
                        'Content-Type': 'application/json',
                        'X-CSRF-TOKEN': document.querySelector('meta[name="csrf-token"]').getAttribute('content')
                    },
                    body: JSON.stringify({
                        date: dateInput.value
                    })
                })
                .then(response => response.json())
                .then(data => {
                    if (data.attendances && data.attendances.length > 0) {
                        attendanceTable.style.display = 'block';
                        attendanceTableBody.innerHTML = '';

                        data.attendances.forEach(attendance => {
                            const row = document.createElement('tr');

                            // Create and append cells safely to prevent XSS
                            const nameCell = document.createElement('td');
                            nameCell.className = 'px-3 py-3 sm:px-6 sm:py-4 whitespace-nowrap text-sm text-gray-900';
                            nameCell.textContent = attendance.user.name;

                            const classCell = document.createElement('td');
                            classCell.className = 'px-3 py-3 sm:px-6 sm:py-4 whitespace-nowrap text-sm text-gray-500';
                            classCell.textContent = attendance.schedule?.classModel?.name || 'N/A';

                            const timeCell = document.createElement('td');
                            timeCell.className = 'px-3 py-3 sm:px-6 sm:py-4 whitespace-nowrap text-sm text-gray-500';
                            timeCell.textContent = attendance.check_in_time ? new Date(attendance.check_in_time).toLocaleTimeString() : 'N/A';

                            const statusCell = document.createElement('td');
                            statusCell.className = 'px-3 py-3 sm:px-6 sm:py-4 whitespace-nowrap text-sm text-gray-500';

                            const statusSpan = document.createElement('span');
                            statusSpan.className = 'px-2 inline-flex text-xs leading-5 font-semibold rounded-full';

                            // Set status-specific classes
                            if (attendance.status === 'present') {
                                statusSpan.className += ' bg-green-100 text-green-800';
                            } else if (attendance.status === 'late') {
                                statusSpan.className += ' bg-yellow-100 text-yellow-800';
                            } else if (attendance.status === 'absent') {
                                statusSpan.className += ' bg-red-100 text-red-800';
                            } else {
                                statusSpan.className += ' bg-blue-100 text-blue-800';
                            }

                            statusSpan.textContent = attendance.status.charAt(0).toUpperCase() + attendance.status.slice(1);
                            statusCell.appendChild(statusSpan);

                            const notesCell = document.createElement('td');
                            notesCell.className = 'px-3 py-3 sm:px-6 sm:py-4 whitespace-nowrap text-sm text-gray-500';
                            notesCell.textContent = attendance.notes || '';

                            row.appendChild(nameCell);
                            row.appendChild(classCell);
                            row.appendChild(timeCell);
                            row.appendChild(statusCell);
                            row.appendChild(notesCell);

