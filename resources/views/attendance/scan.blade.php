            function selectAttendanceStatus(status) {
                document.getElementById('statusModal').style.display = 'none';

                // Get student data from the modal
                const studentName = document.querySelector('#studentInfo .font-medium strong').textContent;

                // Show processing message
                resultDiv.innerHTML = '<p class="text-blue-600">Updating attendance for ' + studentName + ' status to ' + status.toUpperCase() + '...</p>';
                resultContainer.style.display = 'block';

                // First, find the schedule for the student on the given date
                fetch('/attendance/get', {
                    method: 'POST',
                    headers: {
                        'Content-Type': 'application/json',
                        'X-CSRF-TOKEN': document.querySelector('meta[name="csrf-token"]').getAttribute('content')
                    },
                    body: JSON.stringify({
                        date: window.currentDate,
                        user_id: window.currentStudentId  // Only get this student's attendance
                    })
                })
                .then(response => response.json())
                .then(scheduleData => {
                    if (scheduleData.attendances && scheduleData.attendances.length > 0) {
                        const attendanceId = scheduleData.attendances[0].id;
                        // Update the existing attendance record
                        fetch('/attendance/store', {
                            method: 'POST',
                            headers: {
                                'Content-Type': 'application/json',
                                'X-CSRF-TOKEN': document.querySelector('meta[name="csrf-token"]').getAttribute('content')
                            },
                            body: JSON.stringify({
                                user_id: window.currentStudentId,
                                schedule_id: scheduleData.attendances[0].schedule_id,
                                status: status,  // Update to the selected status
                                notes: scheduleData.attendances[0].notes || ''
                            })
                        })
                        .then(response => response.json())
                        .then(updateData => {
                            if (updateData.success) {
                                resultDiv.innerHTML = '<p class="text-green-600">Attendance status updated successfully to ' + status.toUpperCase() + '</p>';
                                if (updateData.attendance) {
                                    resultDiv.innerHTML += '<p class="text-blue-600">New Status: ' + updateData.attendance.status.toUpperCase() + '</p>';
                                }
                            } else {
                                resultDiv.innerHTML += '<p class="text-red-600">Error updating status: ' + updateData.message + '</p>';
                            }
                            // Refresh attendance list
                            fetchAttendance();
                        })
                        .catch(error => {
                            resultDiv.innerHTML += '<p class="text-red-600">Error updating attendance: ' + error.message + '</p>';
                            console.error('Update attendance error:', error);
                        });
                    } else {
                        resultDiv.innerHTML += '<p class="text-red-600">Error: Could not find attendance record to update</p>';
                    }
                })
                .catch(error => {
                    resultDiv.innerHTML += '<p class="text-red-600">Error finding attendance: ' + error.message + '</p>';
                    console.error('Find attendance error:', error);
                });
            }

